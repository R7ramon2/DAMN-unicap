                                </div>

                                <div class="row">
                                    <div class="col-md-12">
                                        <p class="article-conclusion">
                                            A matrícula é renovada a cada semestre letivo, no período estabelecido no Calendário Acadêmico anual da unicap e será efetivada com a confirmação da primeira mensalidade paga e o contrato assinado. A não renovação da matrícula representa abandono de curso.
                                        </p>
                                    </div>
                                </div>
                            </article>
                            <article class="blog-item">
                                <div class="blog-heading">
                                    <h3 class="text-capitalize">Trancamento de Matrícula</h3>
                                </div>

                                <div class="row">
                                    <div class="col-md-12">
                                        <p class="article-conclusion">
                                            Pode ser concedido o trancamento de matrícula para o efeito de, interrompidos temporariamente os estudos, manter o aluno sua vinculação à Faculdade e seu direito à renovação de matrícula para o próximo e consecutivo período letivo, se houver o oferecimento do curso, da série e da turma pretendidos, e ainda, se houver vaga, de acordo com os termos do contrato celebrado entre as partes e de acordo com a análise curricular, devendo, ainda, se adaptar à matriz curricular vigente, quando do seu retorno. O trancamento tem validade, apenas, até o final do período letivo vigente em que foi requerido.<br><br>

                                            <strong>IMPORTANTE</strong>: É necessário que o aluno, ao interromper seus estudos, formalize o trancamento da matrícula, para que haja assim o cancelamento do contrato e cesse a obrigatoriedade de pagamentos.
                                    </div>
                                </div>
                            </article>
                            <article class="blog-item">
                                <div class="blog-heading">
                                    <h3 class="text-capitalize">Cancelamento de Matrícula</h3>
                                </div>

                                <div class="row">
                                    <div class="col-md-12">
                                        <p class="article-conclusion">
                                            O Cancelamento da matrícula poderá ocorrer a requerimento do aluno ou pela unicap, quando de processo disciplinar com aplicação da pena de desligamento. É condição para o cancelamento a quitação das mensalidades até o mês vigente da solicitação e preenchimento de requerimento próprio entregue à Secretaria Acadêmica. Neste caso, o aluno perderá o vínculo com a Instituição e, para retornar aos estudos, terá que candidatar-se a novo Processo Seletivo.
                                    </div>
                                </div>
                            </article>
